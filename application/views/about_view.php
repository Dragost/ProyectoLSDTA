											<h2>Los Secretos de tu Almohada</h2>
											<p>Los secretos de tu almohada es un grupo nacido en el zaragozano barrio de Casetas, de tendencias entre el indie/pop y rock/alternativo. Son un grupo joven y con muchas ganas de demostrar todo lo que son capaces de hacer en directo, con sus versiones de artistas como Amaral, Vetusta Morla, Supersubmarina, Izal o Sidonie, entre otros. 
Está compuesto por Ana María Acón, Jorge Portillo e Ivan García.

Este grupo dio a luz a principios del 2013 en la Casa de Juventud de Casetas, cuando las ideas de formar un grupo consolidado y presentar un proyecto para la iniciativa de "De gira por Casetas" volaban por la cabeza de Ana, quien se encargo de buscar a Pedro como guitarra acompañante en un primer momento. Posteriormente y con sugerencia de Isidro, el profe de guitarra, se incorporó al proyecto Álex, aportando así la percusión. 

A principios de 2014, después de actuar por numerosos bares y pubs durante el año, recogen el premio a "Mejor Versión realizada" en el concurso "The Beatles VS The Rolling Stones" que se realizó en Zaragoza, con su versión de "Help!". Posteriormente completan su formación al conocer a Josevi, quien pasa a aportar el bajo al grupo.
A principios del verano del 2014 entra como guitarra Jorge Portillo. mas tarde, a finales de verano entra Iván García al bajo.</p>
											
